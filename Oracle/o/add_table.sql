1-marcar uma coluna como nao usada

  ALTER TABLE TBEDGE_CLIENTES
  SET UNUSED COLUMN NM_CLIENTE
  CASCADE CONSTRAINTS;

2-eliminar colunas nao usadas

  ALTER TABLE TBEDGE_CLIENTES
  DROP UNUSED COLUMNS CHECKPOINTS 1000;

3-continuar a operacao de eliminacao de colunas

  ALTER TABLE TBEDGE_CLIENTES
  DROP COLUMNS CONTINUE CHECKPOINT 1000;

