CREATE OR REPLACE TRIGGER "SEU".TRG_SEUADL_INCR_I BEFORE
INSERT ON "SEU"."SEUADL" REFERENCING OLD AS OLD NEW AS NEW FOR EACH ROW 
BEGIN
IF :new.SADL_DESCUNID = 'VM' THEN
SELECT SEQ_SEUADL_INSERT.NEXTVAL, SEQ_SEUADL_PROCESSO_VM.NEXTVAL
INTO :new.SADL_NREG, :new.SADL_PROCESSO
FROM DUAL;
END IF;IF :new.SADL_DESCUNID = 'MM' THEN
SELECT SEQ_SEUADL_INSERT.NEXTVAL, SEQ_SEUADL_PROCESSO_MM.NEXTVAL
INTO :new.SADL_NREG, :new.SADL_PROCESSO
FROM DUAL;
END IF;IF :new.SADL_DESCUNID = 'VG' THEN
SELECT SEQ_SEUADL_INSERT.NEXTVAL, SEQ_SEUADL_PROCESSO_VG.NEXTVAL
INTO :new.SADL_NREG, :new.SADL_PROCESSO
FROM DUAL;
END IF;
END;
