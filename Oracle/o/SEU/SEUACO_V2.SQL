CREATE OR REPLACE VIEW "SEU"."SEUACO" ("SACO_IDEMPRESA","SACO_MATRICULA","SACO_NOME","SACO_NASC","SACO_CIC","SACO_RG","SACO_SEXO","SACO_ENDERECO","SACO_COMPLEMENTO","SACO_BAIRRO","SACO_CIDADE","SACO_ESTADO","SACO_CEP","SACO_TELEFONE","SACO_DPTO","SACO_CARGO","SACO_TITULACAO","SACO_EMAIL","SACO_ADMISSAO","SACO_SENHA","SACO_NREG","SACO_CODTIT") AS SELECT CASE
WHEN EMPRESA = '01' AND FILIAL = '01' THEN 1
WHEN EMPRESA = '01' AND FILIAL = '02' THEN 2
WHEN EMPRESA = '04' THEN 3
WHEN EMPRESA = '02' THEN 4
WHEN EMPRESA = '03' THEN 5
END AS SACO_IDEMPRESA,
TO_CHAR(RA_MAT) AS SACO_MATRICULA, RA_NOME AS SACO_NOME, RA_NASC AS SACO_NASC,
RA_CIC AS SACO_CIC, RA_RG AS SACO_RG, RA_SEXO AS SACO_SEXO, RA_ENDEREC AS SACO_ENDERECO,
RA_COMPLEM AS SACO_COMPLEMENTO, RA_BAIRRO AS SACO_BAIRRO, RA_MUNICIP AS SACO_CIDADE,
RA_ESTADO AS SACO_ESTADO, RA_CEP AS SACO_CEP, RA_TELEFON AS SACO_TELEFONE, I3_DESC AS SACO_DPTO,
RJ_DESC AS SACO_CARGO, RA_TITULA_ AS SACO_TITULACAO, RA_EMAIL AS SACO_EMAIL, RA_ADMISSA AS SACO_ADMISSAO,
RA_SENHA AS SACO_SENHA,
ROWNUM AS SACO_NREG,
RA_CODTIT AS SACO_CODTIT
FROM (SELECT RA_NASC, RA_SEXO, RA_NOME, RA_MAT, RA_ENDEREC, RA_COMPLEM, RA_BAIRRO, RA_MUNICIP, RA_ESTADO,
RA_CEP, RA_TELEFON, I3_CUSTO,I3_DESC, RJ_DESC, RA_CIC, RA_RG, RA_TITULA_, RA_EMAIL, '01' EMPRESA,
RA_ADMISSA, '01' FILIAL, RA_SENHA,RA_CODTIT
FROM SRA010, SIGA.SI3010, SIGA.SRJ010
WHERE RA_FILIAL = I3_FILIAL
AND RA_FILIAL = '01'
AND RA_CC = I3_CUSTO
AND RA_CODFUNC = RJ_FUNCAO
AND RJ_FILIAL = ' '
AND RA_SITFOLH <> 'D'
AND ltrim(rtrim(I3_DESC)) <> ' '
AND SRA010.D_E_L_E_T_ <> '*'
AND SI3010.D_E_L_E_T_ <> '*'
AND SRJ010.D_E_L_E_T_ <> '*'
UNION
SELECT RA_NASC, RA_SEXO, RA_NOME, RA_MAT, RA_ENDEREC, RA_COMPLEM, RA_BAIRRO, RA_MUNICIP, RA_ESTADO,
RA_CEP, RA_TELEFON, I3_CUSTO,I3_DESC, RJ_DESC, RA_CIC, RA_RG, RA_TITULA_, RA_EMAIL, '01' EMPRESA,
RA_ADMISSA, '02' FILIAL, RA_SENHA,RA_CODTIT
FROM SRA010, SIGA.SI3010, SIGA.SRJ010
WHERE RA_FILIAL = I3_FILIAL
AND RA_FILIAL = '02'
AND RA_CC = I3_CUSTO
AND RA_CODFUNC = RJ_FUNCAO
AND RJ_FILIAL = ' '
AND RA_SITFOLH <> 'D'
AND ltrim(rtrim(I3_DESC)) <> ' '
AND SRA010.D_E_L_E_T_ <> '*'
AND SI3010.D_E_L_E_T_ <> '*'
AND SRJ010.D_E_L_E_T_ <> '*'
UNION
SELECT RA_NASC, RA_SEXO, RA_NOME, RA_MAT, RA_ENDEREC, RA_COMPLEM, RA_BAIRRO, RA_MUNICIP, RA_ESTADO,
RA_CEP, RA_TELEFON, I3_CUSTO,I3_DESC, RJ_DESC, RA_CIC, RA_RG, RA_TITULA_, RA_EMAIL, '02' EMPRESA,
RA_ADMISSA, '01' FILIAL, RA_SENHA,RA_CODTIT
FROM SRA020, SIGA.SI3020, SIGA.SRJ010
WHERE RA_FILIAL = I3_FILIAL(+)
AND RA_CC = I3_CUSTO(+)
AND RA_CODFUNC = RJ_FUNCAO(+)
AND RJ_FILIAL(+) = '  '
AND RA_SITFOLH <> 'D'
AND ltrim(rtrim(I3_DESC)) <> ' '
AND SRA020.D_E_L_E_T_ <> '*'
AND SI3020.D_E_L_E_T_(+) <> '*'
AND SRJ010.D_E_L_E_T_(+) <> '*'
UNION
SELECT RA_NASC, RA_SEXO, RA_NOME, RA_MAT, RA_ENDEREC, RA_COMPLEM, RA_BAIRRO, RA_MUNICIP, RA_ESTADO,
RA_CEP, RA_TELEFON, I3_CUSTO,I3_DESC, RJ_DESC, RA_CIC, RA_RG, RA_TITULA_, RA_EMAIL, '03' EMPRESA,
RA_ADMISSA, '01' FILIAL, RA_SENHA,RA_CODTIT
FROM SRA030, SIGA.SI3030, SIGA.SRJ010
WHERE RA_FILIAL = I3_FILIAL(+)
AND RA_CC = I3_CUSTO(+)
AND RA_CODFUNC = RJ_FUNCAO(+)
AND RJ_FILIAL(+) = '  '
AND RA_SITFOLH <> 'D'
AND ltrim(rtrim(I3_DESC)) <> ' '
AND SRA030.D_E_L_E_T_ <> '*'
AND SI3030.D_E_L_E_T_(+) <> '*'
AND SRJ010.D_E_L_E_T_(+) <> '*'
UNION
SELECT RA_NASC, RA_SEXO, RA_NOME, RA_MAT, RA_ENDEREC, RA_COMPLEM, RA_BAIRRO, RA_MUNICIP, RA_ESTADO,
RA_CEP, RA_TELEFON, I3_CUSTO,I3_DESC, RJ_DESC, RA_CIC, RA_RG, RA_TITULA_, RA_EMAIL, '04' EMPRESA,
RA_ADMISSA, '01' FILIAL, RA_SENHA,RA_CODTIT
FROM SRA040, SIGA.SI3040, SIGA.SRJ010
WHERE RA_FILIAL = I3_FILIAL(+)
AND RA_CC = I3_CUSTO(+)
AND RA_CODFUNC = RJ_FUNCAO(+)
AND RJ_FILIAL(+) = '  '
AND RA_SITFOLH <> 'D'
AND ltrim(rtrim(I3_DESC)) <> ' '
AND SRA040.D_E_L_E_T_ <> '*'
AND SI3040.D_E_L_E_T_(+) <> '*'
AND SRJ010.D_E_L_E_T_(+) <> '*'
)TMP 
