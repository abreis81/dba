1-movendo uma coluna de uma tabela no oracle 8i

  ALTER TABLE TBEDGE_CLIENTES 
  DROP COLUMN NM_CLIENTE 
  CASCADE CONSTRAINTS CHECKPOINT 1000;

obs: a opcao CHECKPOIN 1000 forca um checkpoint a cada 1000 linhas minimizando a utilizacao
     dos segmentos de rollback.

2-se ocorrer problema durante a operacao a tabela eh marcada como INVALID e a operacao devera
  ser concluida com:

  ALTER TABLE TBEDGE_CLIENTES 
  DROP COLUMNS CONTINUE;

